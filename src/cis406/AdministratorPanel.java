    public void clickSave() {
        Configuration config = new Configuration(Integer.parseInt(txtTimeout.getText()),
                Integer.parseInt(txtPasswordInterval.getText()), Integer.parseInt(txtPasswordLength.getText()));

        Writer output = null;
        File file = new File("internapp.config");
        try {
                output = new BufferedWriter(new FileWriter(file));
                output.write(config.getPassword_interval() + "|"
                                + config.getPassword_length() + "|"
                                + config.getSession_timeout());
                output.close();
                System.out.println("Your file has been written");
        } catch (Exception e) {

        }
    }

