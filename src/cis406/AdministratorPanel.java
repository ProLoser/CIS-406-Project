    private void btnReadUserLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReadUserLogActionPerformed

        DefaultListModel model = new DefaultListModel();
        try {
            ResultSet rs = Database.execute("select distinct user_id from user_log");
            while (rs.next()) {
                int pos = model.getSize();
                model.add(pos, rs.getString("user_id"));
            }

        } catch (Exception e) {
            System.out.println("Could not execute query");
            System.out.println(e.getMessage());
        }

        lstUsers.setModel(model);
}//GEN-LAST:event_btnReadUserLogActionPerformed

