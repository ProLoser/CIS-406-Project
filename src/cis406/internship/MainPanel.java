    public void clickSave() {
        if (activeCard.equals("Edit") && editPanel.save()) {
            browsing();
        }
    }

    public void editing() {
        CardLayout cl = (CardLayout) (getLayout());
        cl.show(this, "Edit");
        activeCard = "Edit";
        editPanel.reloadComboBoxes();
    }

    public void browsing() {
        CardLayout cl = (CardLayout) (getLayout());
        cl.show(this, "Browse");
        activeCard = "Browse";
        browsePanel.loadTable();
    }

